-- [[ Basic Keymaps ]]
-- See :help vim.keymap.set

-- Set up config sync via Lua module (see lua/sync.lua)
local nimv_sync = require 'sync'
vim.keymap.set('n', '<leader>u', nimv_sync.commit_and_push_config_changes, { desc = '[U]pload the nvim config to GitHub' })

-- Pressing <Esc> in normal mode will clear search highlighting
vim.keymap.set('n', '<Esc>', '<cmd>nohlsearch<CR>')

-- Diagnostic keymaps
-- <leader>q: Open list of diagnostics in location list (see :help diagnostic)
vim.keymap.set('n', '<leader>q', vim.diagnostic.setloclist, { desc = 'Open diagnostic [Q]uickfix list' })

-- Easier shortcut to exit terminal mode: double ESC (instead of <C-\><C-n>)
-- Helpful for terminal users new to Neovim
vim.keymap.set('t', '<Esc><Esc>', '<C-\\><C-n>', { desc = 'Exit terminal mode' })

-- Close and Save with leader
vim.keymap.set('n', '<leader>m', '<cmd>update<CR>', { desc = 'Write File', silent = true })
vim.keymap.set('n', '<leader>M', '<cmd>w !sudo tee %:p:S >/dev/null | setlocal nomodified<CR>', { desc = 'Write File with sudo' })
vim.keymap.set('n', '<leader>n', '<cmd>quit<CR>', { desc = 'Quit File', silent = true })
vim.keymap.set('n', '<leader>N', '<cmd>quitall<CR>', { desc = 'Quit Neovim', silent = true })

-- Keybinds for fast window navigation with Ctrl+Arrow keys
-- See :help wincmd for details on window commands
vim.keymap.set('n', '<C-left>', '<C-w><C-h>', { desc = 'Move focus to the left window' })
vim.keymap.set('n', '<C-right>', '<C-w><C-l>', { desc = 'Move focus to the right window' })
vim.keymap.set('n', '<C-down>', '<C-w><C-j>', { desc = 'Move focus to the lower window' })
vim.keymap.set('n', '<C-up>', '<C-w><C-k>', { desc = 'Move focus to the upper window' })

-- Keybind for scrolling up or down with page-up and page-down
vim.keymap.set('n', '<PageUp>', '<C-u>', { desc = 'Scroll up half a page' })
vim.keymap.set('n', '<PageDown>', '<C-d>', { desc = 'Scroll down half a page' })

-- Swap * and # (Thats the moving to next occurance of the word)
vim.keymap.set('n', '*', '#', { desc = 'search backward for the identifier under the cursor', noremap = true })
vim.keymap.set('n', '#', '*', { desc = 'search forward for the identifier under the cursor', noremap = true })
vim.keymap.set('n', 'g*', 'g#', { desc = 'search backward for the identifier under the cursor', noremap = true })
vim.keymap.set('n', 'g#', 'g*', { desc = 'search forward for the identifier under the cursor', noremap = true })

-- Insert current date and time in format yyyy-mm-dd HH:MM
vim.keymap.set('n', '<leader>id', function()
  local date = tostring(os.date '%Y-%m-%d %H:%M') -- get current date and time
  vim.api.nvim_put({ date }, 'c', true, true) -- put it below the current line
end, { desc = '[I]nsert current [d]ate and time' })

