-- [[ Custom Tabline ]]
-- Custom tab names
vim.o.tabline = '%!v:lua.MyTabline()'

function _G.MyTabline()
  local s = ''
  for i = 1, vim.fn.tabpagenr '$' do
    local winnr = vim.fn.tabpagewinnr(i)
    local buflist = vim.fn.tabpagebuflist(i)
    local bufnr = buflist[winnr]
    local bufname = vim.fn.fnamemodify(vim.fn.bufname(bufnr), ':t')

    -- Here you decide how the tab should be named
    if bufname == '' then
      bufname = '[No Name]'
    end

    s = s .. '%' .. i .. 'T' .. ' ' .. bufname .. ' '
  end
  return s
end
-- Keybinds for switching tabs with Ctrl+1-9
for i = 1, 9 do
  vim.keymap.set('n', '<C-' .. i .. '>', function()
    vim.cmd('tabn ' .. i) -- go to tab i
  end, { noremap = true, silent = true })
end

-- [[ Basic Autocommands ]]
-- See :help lua-guide-autocommands for details on autocommand usage.

-- Jump to previous tab when closing a tab
vim.api.nvim_create_autocmd('TabClosed', {
  callback = function()
    -- always move back one tab when a tab closes
    local prev = vim.fn.tabpagenr() - 1
    if prev >= 1 then
      vim.cmd('tabnext ' .. prev)
    end
  end,
})

